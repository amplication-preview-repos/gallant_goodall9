import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { LoanStatusList } from "./loanStatus/LoanStatusList";
import { LoanStatusCreate } from "./loanStatus/LoanStatusCreate";
import { LoanStatusEdit } from "./loanStatus/LoanStatusEdit";
import { LoanStatusShow } from "./loanStatus/LoanStatusShow";
import { LoanList } from "./loan/LoanList";
import { LoanCreate } from "./loan/LoanCreate";
import { LoanEdit } from "./loan/LoanEdit";
import { LoanShow } from "./loan/LoanShow";
import { PaymentList } from "./payment/PaymentList";
import { PaymentCreate } from "./payment/PaymentCreate";
import { PaymentEdit } from "./payment/PaymentEdit";
import { PaymentShow } from "./payment/PaymentShow";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"LenderBackend"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="LoanStatus"
          list={LoanStatusList}
          edit={LoanStatusEdit}
          create={LoanStatusCreate}
          show={LoanStatusShow}
        />
        <Resource
          name="Loan"
          list={LoanList}
          edit={LoanEdit}
          create={LoanCreate}
          show={LoanShow}
        />
        <Resource
          name="Payment"
          list={PaymentList}
          edit={PaymentEdit}
          create={PaymentCreate}
          show={PaymentShow}
        />
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
      </Admin>
    </div>
  );
};

export default App;
